/** Normaliza una etiqueta 'libre' del xlsx a una clave del catálogo, o null si no matchea. */
export function matchTagByLabel(raw: string, groupKey?: string): Tag | null {
  const needle = raw.trim().toLowerCase();
  const pool = groupKey ? TAGS_BY_GROUP[groupKey] ?? [] : TAGS;
  return pool.find((t) => t.label.toLowerCase() === needle) ?? null;
}

